#include "Item.h"

Item::Item()
{
}


/******************************************************************************/
/*!
\brief
Initialise the Item here.
*/
/******************************************************************************/
Item::Item(ITEM_TYPE type, int amt)
{
	this->itemType = type;
	this->amtOfItems = amt;
}

Item::~Item()
{
}

/******************************************************************************/
/*!
\brief
Sets the item's type.
*/
/******************************************************************************/
void Item::setType(ITEM_TYPE type)
{
	this->itemType = type;
}

/******************************************************************************/
/*!
\brief
Sets the Item's count.
*/
/******************************************************************************/
void Item::setAmt(int amt)
{
	this->amtOfItems = amt;
}

/******************************************************************************/
/*!
\brief
returns the item's type.
*/
/******************************************************************************/
ITEM_TYPE Item::getType()
{
	return this->itemType;
}

/******************************************************************************/
/*!
\brief
returns the item's count.
*/
/******************************************************************************/
int Item::getAmt()
{
	return this->amtOfItems;
}
